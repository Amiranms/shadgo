var strToReprMap = map[string]string{
	"0": Zero,
	"1": One,
	"2": Two,
	"3": Three,
	"4": Four,
	"5": Five,
	"6": Six,
	"7": Seven,
	"8": Eight,
	"9": Nine,
	":": Colon,
}

func getNumStrRepr(value string) (string, error) {
	val, ok := strToReprMap[value]
	if !ok {
		return "", errors.New("not valid value")
	}
	return val, nil
}

